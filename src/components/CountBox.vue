<template>
  <div class="count-box">
    <button class="minus" @click="handleSubCount">-</button>
    <input type="text" class="inp" :value="value" @change="handleChange">
    <button class="add" @click="handleAddCount">+</button>
  </div>
</template>

<script>
export default {
  props: {
    value: {
      type: Number,
      default: 1
    }
  },
  methods: {
    handleSubCount () {
      if (this.value > 1) this.$emit('input', this.value - 1)
    },
    handleAddCount () {
      this.$emit('input', this.value + 1)
    },
    handleChange (e) {
      const num = +e.target.value.trim()
      if (isNaN(num) || num < 1 || num % 1 !== 0) {
        e.target.value = this.value
        return
      }
      this.$emit('input', num)
    }
  }
}
</script>

